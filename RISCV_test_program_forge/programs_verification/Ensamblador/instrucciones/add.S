.section .text
.globl _start
_start:
        csrrwi x0,6,0x1F

#------------------------Suma Positivo con Positivo----------------------
#----------------------------------1000+1000=0---------------------------
#------------------------------------------------------------------------
        addi ra,x0,1000
        addi sp,x0,1000
        add s0,ra,sp
#------------------------Suma Positivo con negativo----------------------
#----------------------------------1000-1000=0---------------------------
#------------------------------------------------------------------------
        addi ra,x0,1000
        addi sp,x0,-1000
        add s1,ra,sp
#------------------------Suma negativo con negativo----------------------
#-----------------------------*-1000-1000=-2000*----------------------------
#------------------------------------------------------------------------
        addi ra,x0,-1000
        add a0,sp,ra
#------------------------Suma overflow----------------------------------
        li ra,2147483647
        li sp,2147483647
        add a1,sp,ra
#-----------------------------------------------------------
#-----------------------suma consigo mismo-----------------------
        addi a2,x0,2
        add a2,a2,a2
        add a2,a2,a2
        add a2,a2,a2
#------------------sumas normales-------------------------
        li t6,0x00000300
        jalr x0,0(t6)
#-------------------Resultado--------------------------------------
#ra=7FFFFFFF
#sp=7FFFFFFF
#s0=000007D0
#s1=00000000
#a0=FFFFF830
#a1=FFFFFFFE
#a2=00000010
